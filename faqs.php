  <h4 class="expanded_content">Research Assistantships</h4>
  <div style="display: none;">
    <p>We would be glad to provide research assistantship to potential 
Ph.D. students, and exceptional M.S. students. In the process of 
selecting research assistants, research interests are the most important
 factor, which is a mutual match between RA and advisor. You can find 
useful information on our "Research" section and "Publications". The 
research on wireless networking in our group draws on computer networks,
 wireless communications, security issues (cryptography), network 
topology (graph theory), randomness in networks (stochastic processes), 
simulations tools (NS2 and OPNET), and drivers (hands-on experience 
building wireless networks). The more you know about these areas, the 
easier it will be for you to make progress. If you are seriously 
considering working with me, you might want to read some of my latest 
publications, show me how you understand the problem by pointing out 
their limitations. Regarding whether there are RA positions or not, 
students are encouraged to contact me. Sometimes, I might not respond to
 your request immediately; however, I will keep your files on my waiting
 list (this is not just for courtesy). Once we have positions, you will 
be contacted. </p>
<p>As of October 2008, we have two opennings for research assistants,
 preferrably students with either solid theoretical background on 
networking or system integration of hardware and software for 
power-system communications. This is part of our efforts on the newly 
funded NSF Engineering Research Center (ERC)  <a href="http://www.freedm.ncsu.edu/" target="_blank">FREEDM</a> at NC State University.</p>
  </div>

  <h4 class="expanded_content">Independent Study</h4>
  <div style="display: none;">
    <p>In the department of Electrical and Computer Engineering, 
independent study are open to senior Master students (that is not 
first-semester or first-year graduate students) and junior Ph.D. 
students. Independent sstudy projects are one-semester, letter-graded, 
courses, report-based courses. Students who are interested in research, 
have a solid background on networking, and know how to work on 
loosely-defined projects are encouraged to contact faculty members for 
independent study. The basic requirements of conducting independent 
study are: 1) your cumulative GPA is above 3.3; 2) you have taken at 
least 3 networking related courses; 3) you are prepared to dedicate 10 
hours per week on the project.</p>
